    private void saveButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_saveButtonMouseClicked
        var response = ComunasC.modificarComuna(this.idLabel.getText(), this.comunaNameTF.getText(), this.comunaCodeTF.getText(), this.comunaStatusCB.getSelectedIndex() );
        if( response )
            this.resultMessage.setText("Comuna modificada exitosamente");
        else
            this.resultMessage.setText("No fue posible modificar la comuna");
    }//GEN-LAST:event_saveButtonMouseClicked

